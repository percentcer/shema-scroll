#!/usr/bin/env node
// Merge generated Hebrew skeleton + hand-authored annotations into a TS module.
// Usage: node tools/emit-paragraph.mjs <skeleton.json> <pid> <annotations.json> <out.ts>
import { readFileSync, writeFileSync } from 'node:fs';

const [skelFile, pid, annFile, out] = process.argv.slice(2);
const skel = JSON.parse(readFileSync(skelFile, 'utf8'));
const ann = JSON.parse(readFileSync(annFile, 'utf8'))[pid];
if (!ann) throw new Error(`no annotations for ${pid}`);

// Normalize ASCII apostrophes to curly (house style; also keeps TS strings clean).
const esc = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, '’');

const verses = skel.map((v) => {
  const a = ann.verses[v.id];
  if (!a) throw new Error(`no annotations for verse ${v.id}`);
  if (a.translit.length !== v.words.length) {
    throw new Error(`${v.id}: ${v.words.length} words but ${a.translit.length} translits`);
  }
  const words = v.words.map((w, i) => {
    const n = i + 1;
    const parts = [`id: '${w.id}'`, `hePointed: '${w.hePointed}'`];
    const isDivine = w.flags?.divineName;
    parts.push(`translit: '${esc(isDivine ? 'a-do-NAI' : a.translit[i])}'`);
    const gloss = a.gloss?.[String(n)];
    if (gloss) parts.push(`gloss: '${esc(gloss)}'`);
    const flags = [];
    if (isDivine) flags.push('divineName: true');
    if (w.flags?.maqafNext) flags.push('maqafNext: true');
    if (flags.length) parts.push(`flags: { ${flags.join(', ')} }`);
    return `        { ${parts.join(', ')} },`;
  });
  return `      {
        id: '${v.id}',
        ref: '${v.ref}',
        sefariaUrl: '${v.sefariaUrl}',
        english: '${esc(a.english)}',
        words: [
${words.join('\n')}
        ],
      },`;
});

const facts = ann.facts
  .map(
    (f) => `      { id: '${f.id}', anchorWord: '${f.anchorWord}', body: '${esc(f.body)}' },`,
  )
  .join('\n');

const ts = `import type { ShemaParagraph } from './types';

/**
 * GENERATED by tools/emit-paragraph.mjs — Hebrew bytes come straight from the
 * archived Sefaria JSON via tools/build-corpus.mjs. Edit the annotations file
 * (tools/annotations-p2p3.json) and re-run rather than editing Hebrew here.
 */
export const ${pid}: ShemaParagraph = {
  id: '${pid}',
  nickname: '${esc(ann.nickname)}',
  mode: '${ann.mode}',
  audioTrack: '${ann.audioTrack}',
  meaningCard: {
    id: 'card-${pid}',
    title: '${esc(ann.meaningCard.title)}',
    body: '${esc(ann.meaningCard.body)}',
    anchorVerse: '${ann.meaningCard.anchorVerse}',
  },
  facts: [
${facts}
  ],
  verses: [
${verses.join('\n')}
  ],
};
`;
writeFileSync(out, ts);
console.log(`wrote ${out} (${skel.reduce((a, v) => a + v.words.length, 0)} words)`);
